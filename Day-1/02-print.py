# Print function Examples

# Prints the statement line-by-line. If you want the statements to be in different lines
print("My name is Manzoor")  
print("I am a DevOps Engg")

# Prints in a single line.
print("My name is Manzoor", "I am a DevOps Engg")

# Print numbers
print(41)
print(31)

# Perform Calculation using print function
print(41+31)
print(2*3)

# Print the data type of a variable
name = "Arhan"
age = 10
height = 4.5

print(type(name))    # output  <class 'str'>
print(type(age))     # output  <class 'int'>
print(type(height))  # output  <class 'float'>
