# Example while Loop

count = 1          # Iterator. The var is Iterator

while count <= 5:  # Iteration. Loop is iteration
  print("Hello")
  count += 1

print(count)       # Prints 6

# Print the number
i = 1 

while i <= 5:
  print(i)
  count += 1

# Print the number in reverse  5,4,3,2,1

j = 5

while j > 0:
  print(j)
  j -= 1
