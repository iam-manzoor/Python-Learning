# Example of range function

seq = 5

for i in seq:
  print(i)

for num in range(10):
  print(num)
  
for num in range(1,10):
  print(num)

for num in range(1,10,2):
  print(num)

# print even numbers

for num in range(2,100,2):
  print(num)

# print odd numbers

for num in (1,100,2):
  print(num)

