# Print function examples

# Default space between words sep = " "
print("Muhammad","Arhan")     # o/p Muhammad Arhan. By default print add space between two words when its seperated by comma

# end = "\n". Both the words printed in two different lines because "\n" is the end statement
print("Muhammad")
print("Arhan")

print("Muhammad", end="$")  # Prints Muhammad$Arhan. end value can be anything eg " ", "&" etc..
print("Arhan")
